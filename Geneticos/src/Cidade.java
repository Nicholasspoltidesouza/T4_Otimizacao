public class Cidade {
    private double x;
    private double y;
    private String nome;

    public Cidade(double x, double y, String nome) {
        this.x = x;
        this.y = y;
        this.nome = nome;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public String getNome() {
        return nome;
    }
}
